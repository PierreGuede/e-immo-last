
import { db } from "@/lib/db";
import HotelInfoComponent from "./components/hotelInfoComponents";
import { hotelInfoInterface } from "../config/HotelInterface";

export default async function Component({ params }: { params: { id: string } }) {
  const hotel :hotelInfoInterface  = await  db.hotel.findUnique({
    where: { id : params.id },
    include: { Appartement :true, city: true,  }
  });

  return (
<div>
  <HotelInfoComponent hotelInfo={hotel} />

</div>
    
  )
}

